DROP TABLE UORG_SIORG CASCADE CONSTRAINTS PURGE;
DROP TABLE UORG_SIORG_CONTATO CASCADE CONSTRAINTS PURGE;
DROP TABLE UORG_SIORG_ENDERECO CASCADE CONSTRAINTS PURGE;

CREATE TABLE UORG_SIORG(
    id_uorg_siorg NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NR_UORG VARCHAR2(20) NOT NULL,
    NR_UORG_PAI VARCHAR2(10) NOT NULL,
    NR_ORGAO VARCHAR2(10) NOT NULL,
    DS_TIPO VARCHAR2(50) NOT NULL,
    NM_UORG VARCHAR2(150) NOT NULL,
    SG_UORG VARCHAR2(20),
    DS_AREA_ATUACAO VARCHAR2(50),
    CONSTRAINT pk_uorg_siorg PRIMARY KEY(nr_uorg)
    );

CREATE TABLE UORG_SIORG_CONTATO(
    id_uorg_siorg_contato NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_UORG VARCHAR2(20) NOT NULL,
    NR_TELEFONE VARCHAR2(30),
    NM_EMAIL VARCHAR2(100),
    NM_SITE VARCHAR2(150),
    CONSTRAINT pk_uorg_siorg_contato PRIMARY KEY(id_uorg),
    CONSTRAINT fk_uorg_siorg_contato
        FOREIGN KEY(ID_UORG)
            REFERENCES UORG_SIORG(NR_UORG)
    );
    
CREATE TABLE UORG_SIORG_ENDERECO(
    id_uorg_siorg_endereco NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_UORG VARCHAR2(20) NOT NULL,
    DS_LOGRADOURO VARCHAR2(100),
    NR_ENDERECO NUMBER(5),
    DS_COMPLEMENTO VARCHAR2(150),
    DS_BAIRRO VARCHAR2(100),
    NR_CEP NUMBER(10),
    SG_UF VARCHAR2(3),
    NR_MUNICIPIO NUMBER(10),
    NR_PAIS NUMBER(10),
    DS_TIPO_ENDERECO VARCHAR2(20),
    DS_HORARIO_FUNICIONAMENTO VARCHAR2(60),
    CONSTRAINT pk_uorg_siorg_endereco PRIMARY KEY(ID_UORG),
    CONSTRAINT fk_uorg_siorg_endereco
        FOREIGN KEY(ID_UORG)
            REFERENCES UORG_SIORG(NR_UORG)
    );