CREATE TABLE CARGOS_COMISSIONADOS_SIORG(
    ID_CARGOS_COMISSIONADOS_SIORG NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NM_NATUREZA_JURIDICA VARCHAR2(50),
    DS_CATEGORIA_UNIDADE VARCHAR2(150),
    DS_CARGO VARCHAR2(50),
    SG_CARGO VARCHAR2(5),
    NR_CATEGORIA_CARGO NUMBER(3),
    NR_NIVEL_CARGO VARCHAR2(4),
    SG_UNIDADE VARCHAR(10),
    DS_AREA_ATUACAO VARCHAR2(30),
    DS_AUTORIDADE VARCHAR2(20),
    NR_UNIDADE VARCHAR2(10),
    NR_ORGAO VARCHAR2(10),
    NR_UNIDADE_PAI VARCHAR2(10),
    NM_ORGAO VARCHAR2(150),
    NM_UNIDADE VARCHAR2(150),
    NM_CARGO VARCHAR2(150),
    NR_ORDEM NUMBER(7),
    NR_TOTAL NUMBER(3),
